package PaSkCode;

public class Particle {

	 int px, py, velX, velY, radius;
	
	//particle constructor 
	Particle(int radius, int x, int y, int velX, int velY){
		//initial 5 attributes of particles.
		this.radius = radius;
		this.px = x;
		this.py = y;
		this.velX = velX;
		this.velY = velY;
		

	}
}
